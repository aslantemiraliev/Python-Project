while True:
    # Запрос у пользователя о цвете светофора
    color = input("Введите цвет светофора (красный, желтый, зеленый) или 'выход' для завершения: ").strip().lower()

    if color == "выход":
        print("Выход из программы. До свидания!")
        break
    elif color == "красный":
        print("Стой!")
    elif color == "желтый":
        print("Приготовься!")
    elif color == "зеленый":
        print("Иди!")
    else:
        print("Неверный запрос. Пожалуйста, введите правильный цвет (красный, желтый, зеленый) или 'выход' для завершения.")